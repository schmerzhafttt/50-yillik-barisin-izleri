    <h1>KIBRIS BARIŞ HAREKATI</h1>
    <h3>(20/22 Temmuz – 14/16 Ağustos 1974)</h3>

    <p>9282 kilometre kare yüz ölçümü ile Akdeniz’in en büyük üçüncü adası olan Kıbrıs adası Türkiye’ye 65, Yunanistan’a 965 km. uzaklıktadır. Dünya oluşumunun üçüncü zamanında Anadolu ile bitişik olan ada, dördüncü zamanda, İskenderun bölgesinden koparak uzaklaşmıştır. Adanın jeolojik yapısı ile bitki örtüsü İskenderun bölgesi ile benzerlik gösterir.</p>

    <h2>KIBRIS'IN COĞRAFİ VE STRATEJİK ÖNEMİ</h2>
    <p>Kıbrıs adasının kuzeyinde, Akdeniz’e paralel, doğu-batı istikametinde uzanan Beşparmak Dağları yer alır. Sarp ve yalçın kayalardan oluşan Beşparmak Dağları’nın belli geçiş yerlerinin dışında aşılması zordur. Beşparmak Dağları’nın güneyinde, Magaso’dan Güzelyurt’a kadar Meserya ovası uzanır. Adanın güneyinde Trodos Dağı yeralır. Kıbrıs yeryüzünde bakır madeninin ilk işlendiği yerdir. Bu nedenle Kıbrıs’ın adı bakırla ilgilidir. (bakır; Latince cuprum, İngilizce copper)</p>

    <p>Kıbrıs adası, jeopolitik açıdan Akdeniz’de çok öneme haiz bir konumdadır. Türkiye’ye yakınlığı, İskenderun ve Mersin Körfezlerini kontrol etmesi, Akdeniz’in doğusundaki deniz ulaşımı, İsrail ve Suriye’nin liman ve sahillerinin güvenliği, Türk boğazları ve Süveyş Kanalı’nın emniyeti, Akdeniz’in doğusunda doğal gaz ve petrol yatakları ile petrol nakliyatı Kıbrıs adasının önemini artırmaktadır. Kıbrıs adası bu konumu ile; Doğu Akdeniz’de bir uçak gemisi, füzeler için bir rampa, Anadolu’yu güneyden istila için bir atlama taşıdır. Ege adaları ile Anadolu’nun güneyinden de kuşatılmasını tamamlayabilecek önemli bir bölgedir. Türkiye’nin güvenliği için Kıbrıs yüksek bir değer ifade eder.</p>

    <h2>ATATÜRK’ÜN KIBRIS’A BAKIŞI</h2>
    <p>Kıbrıs’ın, stratejik önemini sadece geçmişin şartları içinde değil geleceğin hızla değişen şartları içinde gören büyük asker, en büyük komutan ve devlet adamı Gazi Mustafa Kemal ATATÜRK, Antalya bölgesinde yapılan bir askeri tatbikatta subaylara; “Türkiye’nin yeniden işgal edildiğini ve Türk kuvvetlerinin sadece bu bölgede mukavemet ettiğini farz edelim. İkmal yollarımız ve imkanlarımız nelerdir?” sorusunu yöneltmiştir. Subayların görüş ve düşüncelerini dinleyen ATATÜRK, haritada Kıbrıs adasını işaret ederek: “Efendiler, Kıbrıs düşman elinde bulunduğu sürece, bölgenin ikmal yolları tıkanmıştır. Kıbrıs’a dikkat ediniz. Bu ada bizim için mühimdir” demek suretiyle Kıbrıs’ın Türkiye için taşıdığı stratejik önemini ortaya koymuştur.</p>

    <h2>KIBRIS’TA TARİHSEL GELİŞİMLER</h2>
    <p>Kıbrıs jeopolitik önemi nedeni ile, tarih boyunca çeşitli kavimlerin istilasına uğramıştır. Kıbrıs, M.Ö. 1450 yılından günümüze kadar; Mısırlılar, Hititler, Fenikeliler, Asurlular, Persler, Büyük İskender (Ptoleme Egemenliği), Romalılar, Bizanslılar, Araplar, Haçlılar (Aslan Yürekli Richard), Venedikliler ve Osmanlılar idaresinde kalmıştır. 300 yıl Osmanlı hakimiyetinde kalan Kıbrıs; 1877-78 Osmanlı-Rus Harbi’nde Osmanlıları destekleme karşılığında 1878’de İngiltere’ye geçici olarak bırakılmıştır. İngiltere, I. Dünya Savaşı’nın başında, Kıbrıs’ı bir oldu bittiye getirerek ilhak ettiğini açıklamıştır.</p>

    <h2>ENOSİS VE TÜRK MÜCADELESİ</h2>
    <p>Kıbrıs İngiltere’nin idaresi altında iken, Kıbrıs kilisesi, adayı Yunanistan’a bağlamayı amaçlayan Enosis (birleşme) çabasını yoğunlaştırdı. Enosis hayali Kıbrıs sorununun temelini teşkil eder. Enosis’i gerçekleştirmek için Kıbrıslı Rumlar 1955’te, EOKA adında bir terör örgütünü kurdular.</p>

    <p>Bu örgüt İngilizlere ve Türklere karşı silahlı şiddet hareketlerine başladı. Buna karşılık Türk tarafı da 1958’de, TMT(Türk Mukavemet Teşkilatı) kurarak EOKA ile mücadeleye başladı.</p>

    <h2>KIBRIS’IN BAĞIMSIZLIĞI VE SONRASI</h2>
    <p>Kıbrıs, Londra ve Zürih Garanti ve İttifak Antlaşmalarıyla 1960 yılında bağımsız bir devlet olarak ortaya çıktı. Bu antlaşmaya göre; hükümetin ve icra unsurların %70’i Rum, %30’u Türklerden teşkil edilecek, Bakanlar Kurulu 7 Rum, 3 Türk’ten oluşacaktı. Bir papaz olan Makarios (Mihail Hristodolu Muskos) Cumhurbaşkanı, Dr. Fazıl Küçük de Cumhurbaşkanı Yardımcısı oldu. Garanti Antlaşmaları ile Türkiye, Yunanistan ve İngiltere garantör devlet oldular. İngiltere, iki askeri üs (Agratur-Dikelya) elde etti. Adada Türkiye 650, Yunanistan ise 950 kişilik kuvvet bulundurabilecekti. Garanti antlaşmasına göre Türklere verilen hakları çok gören Makarios Türkleri tamamen yok etmeye kalktı.</p>

    <h2>1974 KIBRIS DARBE VE TÜRKİYE’NİN MÜDAHALESİ</h2>
    <p>Bu arada, Yunanistan adaya gizlice çok sayıda asker çıkardı. Kıbrıslı Türkleri ortadan kaldırmak ve Enosis’i gerçekleştirmek için hazırlanan, “Akritas Planı”nı uygulamaya koymak üzere EOKA çeteleri ve Yunan askerleri 25 Aralık 1963’de saldırıya geçerek çocuk, kadın, yaşlılarda dahil olmak üzere binlerce Türk’ü vahşice katlettiler. Rumların Erenköy’e de saldırmaları üzerine, Türk jetleri Kıbrıs üzerinde uyarı uçuşu yaptılar. Panikleyen Rumlar saldırılarına son vermek zorunda kaldılar.</p>

    <h2>KIBRIS'TA 20 TEMMUZ 1974 ÖNCESİ ASKERİ DURUM</h2>
    <p>Rum kuvvetleri:</p>
    <p>Kıbrıs Rum Kuvvetleri; Rum Milli Muhafız (RMM) ordusu, Rum Polis teşkilatı ve Yunan Alayı’ndan ibarettir. Ayrıca, seferde teşkil edilen Home Guard (HG) taburları ile RMM ordusu takviye edilecektir. Rum ordusu, Yunanlı subaylar tarafından eğitilmekte ve yönetilmektedir. Seferde Rum ordusunun mevcudu 40.000’ne çıkabilmektedir. Bu birliklerin yanı sıra, Makarios’a bağlı 4.000 kişilik “Epikourik” (Taktik Yardım İhtiyat) kuvveti mevcuttu.</p>
    <p>Türk Silahlı Kuvvetleri:</p>
    <p>Kıbrıs Barış Harekatı’na 6’ınci Kolordu Komutanlığı emrinde; 28’inci Motorlu Piyade Tümeni, 39’uncu Piyade Tümeni, Hava İndirme ve Komando Tugayları, Gösteri ve Tatbikat Alayı, Amfibi Deniz Piyade Alayı, Jandarma Komando Taburları, Deniz ve Hava Kuvvetleri birlikleri, Bayraktarlık emrindeki Mücahit Birlikleri, 650 kişilik Kıbrıs Türk Kuvvetleri Alayı ile idari ve lojistik destek birlikleri katılmıştır.</p>
   <br>
   <p>Harekat üç safha olarak planlanmıştı. Birinci safhada hava ve kıyı başının tesisi ve elde bulundurulması, ikinci safhada çıkan ve indirilen birliklerin birleşmesi, üçüncü safhada da harekat alanının genişletilmesi.</p>
    <h2>20 TEMMUZ 1974 – BİRİNCİ KIBRIS BARIŞ HAREKATI</h2>
    <p>20 Temmuz 1974 sabahı, Türk uçaklarının bombardımanından sonra, saat 06.15 den itibaren, hava indirme ve uçar birlik harekatı ile Hava İndirme ve Komando Tugayları Gönyeli ve Kırnı bölgelerine indirilmeye başlanmış, Mersin’den Ertuğrul gemisi ve 33 çıkarma gemisi ile donanmanın koruması altında hareket eden Çakmak Özel Kuvveti de komanda birliklerimizle eş zamanlı olarak Girne’nin batısında dar ve sığ bir plaj olan Pladini (Yavuz) plajına, uçaklarımızın ve deniz topçusunun desteğinde çıkmaya başlamıştı.</p>
    <br>
    <p>SAT komandolarının çıkarma plajının çıkarmaya müsait olduğunu bildirmeleri üzerine, birinci dalga olarak plaja ilk çıkan Amfibi Deniz Piyade Alayı süratle ilerleyerek, Girne – Karava – Geçitköy (Panağra Boğazı) ana asfalt yoluna ulaşmıştı. Çakmak Özel Kuvvetinin diğer unsurları saat 12.00’de plaja çıkarak kıyı başını genişletmeye başlamışlardı.</p>
    <break>
    <p>Gönyeli ovasına paraşütle atlayan Hava İndirme Tugayı, bir taburu ile Kıbrıs Türk Kuvvetleri Alayının batı yanını korurken, geri kalanı ile Dikomo (Dikmen) bölgesini ve Rum Bozdağı’nı ele geçirmek üzere taarruza başlamıştı. Kırnı bölgesine helikopterle inen Komando Tugayı duvar gibi dik dağ yamacını tırmanarak St.Hilarion ve Beyaz Ev bölgesine ulaşmış, bir taburu ile St. Hilarion-Doğru Yol istikametinde, diğer taburu ile Beyaz Ev-Zeytinlik-Girne istikametinde taarruz ederek kıyı başı ile birleşmeye hazırlanıyordu.</p>
    <p>Donanma, sahil bombardımanı yaparak sahile çıkan birliklerimize topçu desteği sağlarken, 2’inci Taktik Hava Kuvvetlerine bağlı savaş uçakları düşmanın ada genelinde askeri hedeflerine taarruz ederek, tecrit ve yakın hava desteği görevlerini yerine getiriyordu.</p>
    <p>20 Temmuz’da Rumlar büyük bir baskına uğramışlardı. Rumlar, Türk Ordusu’nun 1964 ve 1967’de olduğu gibi adaya müdahaleye cesaret edemeyeceği düşüncesinde idiler. Başlangıçta, paraşütle atlayan, helikopterle inen ve kıyıya çıkan birliklerimize etkili bir şekilde müdahale edemediler. Zamanla toparlanan Rum kuvvetleri akşam saatlerinden itibaren birliklerimize karşı harekata başladı.</p>
    <p>20/21 Temmuz gecesi Türk ve Rum kuvvetleri arasında çok çetin çatışmalar yaşandı. Rumların Ortaköy, Gönyeli ve Boğaz Bölgelerini ele geçirerek; Girne-Lefkoşa irtibatını kesmek ve bu suretle; çıkarma yapan birliklerimizle, inen birliklerimizin birleşmesini önlemek amacıyla gece boyunca St.Hilarion, Bozdağ, Dikmen Tepe, Ortaköy ve Gönyeli ile Göçeri bölgelerinde yaptığı saldırılar kahraman Mehmetçikler tarafından her defasında püskürtülmüştü. Kıbrıs’a çıkan ve inen Türk birlikleri ele geçirdikleri yerleri, her ne pahasına olursa olsun elde tutmayı başarmışlardı. Harekatın ilk günlerinde, birliklerimiz hava desteğinin haricinde topçu ve tank desteğinden mahrum idi. Buna rağmen Türk askeri Çanakkale’de, Kurtuluş Savaşı’nda, Kore’de destan yaratan atalarını aratmadılar. Beşparmak Dağlarında, Rumların gece saldırılarına karşı Komando birliklerimizin ölüm-kalım mücadelesi takdire şayandır.</p>
    <p>Türk birlikleri 21 Temmuz’dan itibaren, Rum kuvvetlerine karşı tamamen üstünlük sağlayarak ileri harekatına devam ettiler. 22 Temmuz’da çıkarma yapan birliklerimiz ile birleşme sağlandı. Harekat doğu ve batı yönünde gelişerek Rum hedefleri tek tek ele geçirildi. Girne-Lefkoşa yolu tamamen Türk birliklerinin kontrolüne girdi. Türkiye, BM Güvenlik Konseyi’nin aldığı ateşkes kararını 22 Temmuz 1974 saat 17.00’de kabul edip, uygulamaya koyduğunu ilan etmiştir. 23 Temmuz’da 29 araçlık bir Rum topçu konvoyu Hava İndirme Taburu tarafından pusuya düşürülerek imha edildi.</p>
    <p>Bu gelişmeler üzerine Yunanistan’da cunta, Kıbrıs’ta da Sampson istifa ettiler. BM Güvenlik Konseyi’nin 20 Temmuz 1974 günü aldığı 353 sayılı karara uyarak, üç garantör devlet olan Türkiye, Yunanistan ve İngiltere; Kıbrıs’ta barışı ve anayasal düzeni yeniden kurmak amacıyla 25 Temmuz’da Cenevre’de görüşmelere başladılar. 30 Temmuz’a kadar devam eden bu görüşmelerde; tarafların 8 Ağustos’ta, Cenevre’de tekrar toplanmaları kararı alındı. Bu görüşmeler sonucu yayınlanan “Cenevre Deklarasyon”u ile taraflar; Kıbrıs’ta ayrı iki otonom yönetiminin mevcut olduğunu kabul etmişler, Otonom Türk ve Rum toplumlarının federal bir devlet çatısı altında bir ortak yönetim kurmalarını beyan etmişlerdir.</p>
    <p>Ateşkes ilanından sonra, mevcudu 40.000’ni bulan Türk birlikleri oldukça dar bir alana sıkışmış durumdaydılar. Birliklerin uzun süre bu dar bölgede bekletilmeleri emniyetleri açısından uygun değildi. Ateşkes ile birlikte Türk birliklerinin ilerleyişlerini durdurmaları üzerine adanın her yanındaki binlerce Türk, Rumlar tarafından kuşatılmış, Rumlar Türk köylerindeki savunmasız çoğu çocuk, kadın ve yaşlı olmak üzere yüzlerce Türk’ü topluca ve vahşice öldürmüştü.</p>
    <h2>14 AĞUSTOS 1974- İKİNCİ BARIŞ HAREKATI</h2>
    <p>İkinci Cenevre Konferansı’nda Yunan ve Rum tarafı zaman kazanmak, dünya kamuoyunu Türkiye aleyhine çevirmek için uzlaşmaz bir tutum sergilemeye başladılar. Birinci Cenevre Konferansı’nda alınan kararları dahi dikkate almadılar. İkinci Cenevre Konferansı’nın başarısızlığa uğraması üzerine, Türk Silahlı Kuvvetleri İkinci Barış Harekatı’na başladı. 14 Ağustos günü Saat 06.30’dan itibaren 28 ve 39’uncu Tümenler, Magosa ve Boğaz Deniz üssünü ele geçirmek üzere doğuya doğru taarruza başladılar.</p>
    <p>39’uncu Tümen bölgesindeki İngiliz Tepe ve Kara Tepe, Rum savunmasının bel kemiği durumunda idiler. 39’uncu Piyade Tümen’in birlikleri saat 11.30’da İngiliz Tepe ve Kara Tepe’yi ele geçirdiler. 28’inci Piyade Tümen saat 12.00’ye doğru Mia Milia’yı işgal etti. Saat 15.00 civarında 39’uncu Piyade Tümen Değirmenlik’i, 28’inci Piyade Tümen de Timbu hava alanını ele geçirdi. Türk askeri karşısında çareyi kaçmakta bulan Rumlar mağlubiyetin acısını çıkarmak için; 14 Ağustos’ta Taşkent, Terazi, Atlılar, Muratağa ve Sandallar köylerinde; savunmasız, çoğu çocuk, kadın ve yaşlı olmak üzere yüzlerce Türk’ü topluca ve vahşice öldürmüştür. Adanın diğer kesimindeki Türklere de insanlık dışı, vahşice saldırılar yapılmıştır. Birliklerimiz 14 Ağustos akşama doğru Paşaköy ve Serdarlı’ya girerek soydaşlarımızla kucaklaştılar.</p>
    <p>Kolordu bölgesinin batı kesimini savunmakla görevlendirilen; Kıbrıs Türk Kuvvetleri Alayı, Lefkoşa Sancağı ve Komando Tugayı batı istikametindeki harekata 15 Ağustos’ta başladı.16 Ağustos’ta, doğu ve batı istikametlerinde ileri harekatına devam eden birliklerimiz Birleşmiş Milletler tarafından ilan edilen ateş-kes anlaşmasına kadar Magosa, Lefkoşa ve Lefke hattının kuzeyindeki bölgeyi tamamen kontrol altına almışlardır.</p>
    <h2>Sonuç olarak;</h2>
    <p>Kıbrıs Barış Harekatı ile Kıbrıslı Türklerin can güvenlikleri sağlanmış, Rumların Enosis hayali Akdeniz’in karanlık sularına gömülmüştür. Bu savaşta; 415’i Kara, 65’i Deniz, 5’i Hava ve 13’ü Jandarma olmak üzere 498 Türk askeri şehit olmuş, 1200’de yaralanmıştır. 70 Kıbrıslı Mücahit ve 270 Kıbrıs Türk’ü şehit olmuş, 1000 Kıbrıslı Türk de yaralanmıştır. BM. Barış Gücü askerleri de kayıp vermişti: 3 Avusturyalı asker ölmüş, 24 Avusturyalı, 17 Finlandiyalı, 4 İngiliz ve 3 Kanadalı asker de yaralanmıştı


Türkiye bu harekatı ile kendi güvenliğini ve Kıbrıslı Türklerin güvenliğini tehlikeye atacak girişimlere hiçbir zaman seyirci kalmayacağını dünyaya fiilen kanıtlamış oluyordu.

​

13 Şubat 1975 de Kıbrıs Türk Federe Devlet’i, 15 Kasım 1983 de Kuzey Kıbrıs Türk Cumhuriyeti (KKTC) ilan edildi.

​

Kıbrıs’ta Türk ve Rumlar arasında yapılan tüm görüşmelerde, Rumların uzlaşmaz tutumları nedeniyle günümüze kadar bir sonuç alınamamıştır. Kıbrıs’la ilgili yürütülen görüşmeleri bu uğurda canlarını ortaya koyan gaziler olarak dikkatle izliyoruz. Toprağa düşen şehitlerimizin ve akıtılan kanların dikkate alınacağını umuyor; uğrunda şehit verdiğimiz, kan döktüğümüz toprakları da kutsal bir emanet olarak kabul ediyoruz. Savaşta kazanılan toprağın, herhangi bir nedenle iadesi kabullenemez.

​

Türk silahlı Kuvvetleri’nin Kıbrıs’a yaptığı müdahale; sorunun sebebi değil, Rum-Yunan ikilisinin bugüne kadar adada uyguladıkları yanlış ve tahkirkar politikaların bir sonucudur.

​

Yunanistan’ın Kıbrıs’ı topraklarına katmayı istemesinin asıl amacı, Türkiye’nin Doğu Akdeniz’deki hareket serbestisini kısıtlamak ve Anadolu Yarımadası’nın güneyindeki milli güvenlik kuşağını daraltıp, Türkiye’nin etrafında bir stratejik kuşatma çemberi oluşturarak, onu Anadolu’ya hapsetmektir.

​

Kıbrıs’ta “Kendi kaderini tayin etme” (Self-determinasyon) hakkı söz konusu olduğunda, Ada’da yaşayan Türk halkı, BM Anayasa’sının 73’üncü maddesi esasları çerçevesinde en az Rumlar kadar kendi kaderini tayin etmede söz ve hak sahibidir. Asırlardır Kıbrıs’ta yaşayan Türklerin Ada üzerinde hükümranlık hakları vardır. Bu haklarını Rumlara devretmeleri söz konusu olamaz.

​

Batı, dün olduğu gibi bugünde Yunan-Rum yanlısı tutumunu devam ettirmekte, Yunanistan ve Kıbrıs sorunlarının çözümünde Türk tarafından sürekli ödün istemektedir. Aşağıdaki örnekler, geçmişte Batı’nın, Yunan-Rum yanlısı tutumunu açıkça ortaya koymaktadır.

​

